#include <jpeg2ppm.h>

void display_help(char **argv) {
    fprintf(stderr, BLUE("╔══════════════════════════════════════ JPEG DECODER ═══════════════════════════════════════╗\n"));
    fprintf(stderr, BLUE("║\t\t\t\t\t\t\t\t\t\t\t    ║\n"));
    fprintf(stderr, BLUE("║   Usage: %s [-h] [-v|-hv] [--force-grayscale] <jpeg_file>\t\t\t    ║\n"), argv[0]);
    fprintf(stderr, BLUE("║\t\t\t\t\t\t\t\t\t\t\t    ║\n"));
    fprintf(stderr, BLUE("║   -h\t\t\thelp\t\t\t\t\t\t\t\t    ║\n"));
    fprintf(stderr, BLUE("║   -v\t\t\tverbose mode\t\t\t\t\t\t\t    ║\n"));
    fprintf(stderr, BLUE("║   -hv\t\t\thighly verbose mode\t\t\t\t\t\t    ║\n"));
    fprintf(stderr, BLUE("║   --force-grayscale\tforce grayscale decoding\t\t\t\t\t    ║\n"));
    fprintf(stderr, BLUE("║\t\t\t\t\t\t\t\t\t\t\t    ║\n"));
    fprintf(stderr, BLUE("║   Note: the output file will be saved in the same directory that those of the input file. ║\n"));
    fprintf(stderr ,BLUE("╚═══════════════════════════════════════════════════════════════════════════════════════════╝\n"));
}


int main(int argc, char **argv) {
    if (argc == 1) {
    	/* 
            Si y'a pas au moins un argument en ligne de commandes, on
    	    boude. 
        */
        display_help(argv);
    	return EXIT_FAILURE;
    }

    // Managing options
    bool force_grayscale = false;
    
    if (argc > 2){
        if (optionExists(argc, argv, "-h")){
            display_help(argv);
            return EXIT_SUCCESS;
        }
        
        if (optionExists(argc, argv, "-v")){
            setVerbose(true);
        }
        
        if (optionExists(argc, argv, "-hv")){
            setVerbose(true);
            setHighlyVerbose(true);
        }
        
        if (optionExists(argc, argv, "--force-grayscale")){
            force_grayscale = true;
        }
    }

    // Checking if filename placed correctly in command line
    FILE *input_file = fopen(argv[argc - 1], "r");
    if (!input_file) {
        display_help(argv);
        return EXIT_FAILURE;
    }


    // Now decoding JPEG
    char *filename = argv[argc - 1];

    struct JPEG *jpeg = extract(filename);
    if (jpeg == NULL) return EXIT_FAILURE;

    if (decode_bitstream(jpeg)) {
        free_JPEG_struct(jpeg);
        return EXIT_FAILURE;
    };

    if (IQ(jpeg)) {
        free_JPEG_struct(jpeg);
        return EXIT_FAILURE;
    };

    if (IZZ(jpeg)) {
        free_JPEG_struct(jpeg);
        return EXIT_FAILURE;
    };

    if (IDCT(jpeg)) {
        free_JPEG_struct(jpeg);
        return EXIT_FAILURE;
    };

    if (YCbCr2RGB(jpeg, force_grayscale)) {
        free_JPEG_struct(jpeg);
        return EXIT_FAILURE;
    };

    if (write_ppm(filename, jpeg, force_grayscale)) {
        free_JPEG_struct(jpeg);
        return EXIT_FAILURE;
    };


    fprintf(stderr, GREEN("Image %s décodée avec succès !\n"), filename);


    // Ajouter une musique de victoire + défaite et un gif de chatons
    // On ne gère que 3 composantes au maximum
    // on ne gère pas les fichiers polyglotes (sauf si le jpeg est en début de fichier)
    // Vérifier que la longueur lue dans chaque segment correspond bien à la longueur annoncée du segment
    // thread par composante pour accélérer le traitement pour toutes les étapes après decoding_bitstream
    // Note -2 : il faut forcer la taille de nos variables avec int8_t, int16_t, int32_t, uint8_t, uint16_t, uint32_t pour éviter les problèmes de taille de variables sur différentes architectures matérielles !!!
    // Note 1 : prévoir de repasser toutes les structures en [8][8] ou [64] pour optimiser l'utilisation mémoire et accélérer l'exécution du programme !!!
    // Note 2 : prévoir de reformater nos sorties du mode verbose pour rendre la lecture plus facile&jolie ... s'inspirer de jpeg2blabla ?
    // Note 3 : prévoir de refaire des vrais tests avec toutes ces fonctions modifiées depuis début de semaine !!!
    
    
    // On libère la mémoire
    free_JPEG_struct(jpeg);

    printf("\a");


    return EXIT_SUCCESS;
}
